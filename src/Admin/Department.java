    private void table_departmentMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_departmentMouseClicked
        // TODO add your handling code here:
        
        try 
        {
        
        int row = table_department.getSelectedRow();
        String Table_click = table_department.getModel().getValueAt(row, 0).toString();
        
        String sql = "select * from department where department_NO='"+Table_click+"'";
        pst= con.prepareStatement(sql);
        rs=pst.executeQuery();
        
        if(rs.next())
        {
        String add1 = rs.getString("department_NO");
        id_department.setText(add1);
        String add2 = rs.getString("department_name");
        name_department.setText(add2);
        String add3 = rs.getString("location");
        location_department.setText(add3);
        
        }
        
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null, ex);
        }
        
    }//GEN-LAST:event_table_departmentMouseClicked

