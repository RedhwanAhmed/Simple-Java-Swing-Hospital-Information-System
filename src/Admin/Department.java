    private void save_departmentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_save_departmentActionPerformed
        // TODO add your handling code here:
        
        try 
        {
        
            String ID = id_department.getText();
            String name = name_department.getText();
            String location = location_department.getText();
            
            String sql = "update department SET department_NO='"+ID+"',department_name='"+name+"',location='"+location+"' where department_NO='"+ID+"'";
            
            pst = con.prepareStatement(sql);
            pst.execute();
            
            JOptionPane.showMessageDialog(null, "Updated");
               clear();
               department_table();
            
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null, ex);
        }
        
        
    }//GEN-LAST:event_save_departmentActionPerformed

