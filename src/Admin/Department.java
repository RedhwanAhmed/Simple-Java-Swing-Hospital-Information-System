    private void department_table()
    {
     try
        {
            String sql = "select * from department";
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            table_department.setModel(DbUtils.resultSetToTableModel(rs));
         
        }
        catch(Exception ex)
        {
             JOptionPane.showMessageDialog(null,ex);
        }
        finally 
        {
            try 
            {
                rs.close();
                pst.close();
                
            }
            catch(Exception ex)
            {
                JOptionPane.showMessageDialog(null,ex);
            }
        }
    
    }
    
    
    

