    private void search_doctorKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_search_doctorKeyReleased
        // TODO add your handling code here:
        try 
        {
        
        String sql = "select * from doctor where ID=?";
        pst = con.prepareStatement(sql);
        pst.setString(1, search_doctor.getText());
        
        rs = pst.executeQuery();
        
        if(rs.next())
        {
        String add1 = rs.getString("ID");
        id_doctor_info.setText(add1);
        String add2 = rs.getString("name");
        name_doctor_info.setText(add2);
        String add3 = rs.getString("room");
        room_doctor_info.setText(add3);
        String add4 = rs.getString("hp_no");
        hp_doctor_info.setText(add4);
        String add5 = rs.getString("Department_no");
        department_doctor_info.setText(add5);
        search_doctor.setText("");
        
        }
       
        
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_search_doctorKeyReleased

