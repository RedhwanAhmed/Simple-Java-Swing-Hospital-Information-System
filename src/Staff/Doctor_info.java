     private void doctor_table()
    {
        try
        {
            String sql = "select * from doctor";
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            table_doctor.setModel(DbUtils.resultSetToTableModel(rs));
        
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null,ex);
            
        }
        
        finally 
        {
            try 
            {
                rs.close();
                pst.close();
                
            }
            catch(Exception ex)
            {}
        
        }
        
        
    
    
    }
    
    
